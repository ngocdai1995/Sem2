    public void loadData() {
        while (tableModel.getRowCount() > 0) {
            tableModel.removeRow(0);
        }
        String search = txtSearch.getText();
        productList = ProductController.searchByName(search);

        double p = (double) productList.size() / limit;
        total_page = (int) Math.ceil(p);
        int start = limit * (index - 1);

        showRow();

        for (int i = start; i < start + limit; i++) {
            if (i < productList.size()) {
                tableModel.setColumnIdentifiers(new Object[]{
                    "STT", "Product_ID", "ProductName", "Price",});
                tableModel.addRow(new Object[]{
                    i + 1,
                    productList.get(i).getId(),
                    productList.get(i).getName(),
                    Utility.getPrice(productList.get(i).getPrice())

                });
            }
        }
        tableProduct.setModel(tableModel);

    }

    private void showRow() {
        pn_page.removeAll();
        pn_page.revalidate();
        pn_page.repaint();

        JButton pre = new JButton();
        pre.setFont(btn_model.getFont());
        pre.setText("<<");
        pn_page.add(pre);

        for (int i = 1; i <= total_page; i++) {
            if (i + 1 < index && i != 1) {
                continue;
            }

            if (i - 1 > index && i != total_page) {
                continue;
            }

            JButton btn = getButton(i);
            pn_page.add(btn);
        }

        JButton next = new JButton();
        next.setFont(btn_model.getFont());
        next.setText(">>");
        pn_page.add(next);

        if (index == 1) {
            pre.setEnabled(false);
        }
        if (index == total_page) {
            next.setEnabled(false);
        }

        pre.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                index--;
                loadData();
            }
        });

        next.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                index++;
                loadData();
            }
        });

    }

    private JButton getButton(int i) {
        JButton btn = new JButton();
        btn.setFont(btn_model.getFont());
        btn.setText("" + i);

        btn.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                index = Integer.parseInt(btn.getText());
                loadData();
            }
        });

        if (index == i) {
            btn.setFont(btn_model1.getFont());
        }
        return btn;
    }

