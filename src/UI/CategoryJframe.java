    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String nameCate = txtCateName.getText();
        String created_at = Utility.getTimeNow("yyyy-MM-dd HH:mm:ss");
        String updated_at = Utility.getTimeNow("yyyy-MM-dd HH:mm:ss");

        if (Validate()) {
            if (position >= 0) {
                cateModel = cateList.get(position);
                position = -1;
                cateModel.setName(nameCate);

                cateModel.setCreated_at(created_at);
                cateModel.setUpdated_at(updated_at);
                JOptionPane.showMessageDialog(this, "Bạn đã cập nhật thành công");
            } else {
                cateModel = new CategoryModel(nameCate, created_at, updated_at);
                 JOptionPane.showMessageDialog(this, "Bạn đã thêm mới thành công");
            }
            CategoryController.save(cateModel);

            cateList = CategoryController.getCategoryList();
            showCategory();
            btnResetActionPerformed(evt);

        }
    }//GEN-LAST:event_btnAddActionPerformed

