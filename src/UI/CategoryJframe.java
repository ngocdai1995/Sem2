    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        position = tableCategory.getSelectedRow();
        if (position == -1) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn danh mục cần xóa");
            return;
        }
        int option = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn xóa danh mục này không");
        if (option == 0 && position >= 0) {
            CategoryController.delete(cateList.get(position).getId());
            position = -1;
            CategoryController.getCategoryList();
            showCategory();
            btnResetActionPerformed(evt);

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

